"use client"

import { Check, Download, FileText, Save } from "lucide-react"

import { useApp } from "@/components/app-store"
import { Badge } from "@/components/ui/badge"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card"
import { Separator } from "@/components/ui/separator"
import { downloadReportPdf } from "@/lib/api"
import type { ScoreResponse } from "@/lib/api"

export function DocumentView({ result }: { result: ScoreResponse }) {
  const { saveCurrentReport, activeReportId } = useApp()

  async function handleDownload() {
    try {
      const blob = await downloadReportPdf(
        result.disease_label,
        result.modality,
        result.targets,
      )
      const url = URL.createObjectURL(blob)
      const a = document.createElement("a")
      a.href = url
      a.download = `mentytarget_${result.disease_label.replace(/\s+/g, "_")}.pdf`
      document.body.appendChild(a)
      a.click()
      document.body.removeChild(a)
      URL.revokeObjectURL(url)
    } catch (err) {
      console.error("Failed to download report:", err)
    }
  }

  const top = [...result.targets].sort((a, b) => b.score - a.score).slice(0, 3)
  const topNames = top.map((t) => t.symbol)
  const avgTractability =
    result.targets.reduce((sum, t) => sum + t.tractability, 0) /
    Math.max(result.targets.length, 1)
  const topEvidenceType = mostCommonEvidence(result.targets)

  return (
    <div className="flex flex-col gap-6">
      <Card>
        <CardContent className="flex flex-col gap-8 px-6 py-8 md:px-10 md:py-10">
          {/* Title block */}
          <header className="border-border flex flex-col gap-2 border-b pb-6">
            <div className="text-primary flex items-center gap-2 text-xs font-medium tracking-wide uppercase">
              <FileText className="size-4" />
              Target Identification Report
            </div>
            <h2 className="text-foreground text-2xl font-semibold tracking-tight">
              {result.disease_label}
            </h2>
            <p className="text-muted-foreground text-sm">
              Generated by MentyTarget &middot;{" "}
              {new Date().toLocaleDateString(undefined, {
                year: "numeric",
                month: "long",
                day: "numeric",
              })}{" "}
              &middot; Model: {result.model}
            </p>
          </header>

          {/* Executive Summary */}
          <section className="flex flex-col gap-3">
            <h3 className="text-foreground text-base font-semibold">
              Executive Summary
            </h3>
            <p className="text-muted-foreground text-sm leading-relaxed">
              MentyTarget analyzed multi-omic evidence for{" "}
              <span className="text-foreground font-medium">
                {result.disease_label}
              </span>{" "}
              and prioritized{" "}
              <span className="text-foreground font-medium">
                {result.targets.length} candidate targets
              </span>{" "}
              for a {result.modality.replace(/_/g, " ")} approach. The strongest
              associations are{" "}
              <span className="text-foreground font-medium">
                {topNames.join(", ")}
              </span>
              , each supported by convergent evidence. The average tractability
              across this target set is{" "}
              <span className="text-foreground font-medium">
                {avgTractability.toFixed(2)}
              </span>
              , with{" "}
              <span className="text-foreground font-medium">
                {topEvidenceType}
              </span>{" "}
              as the dominant evidence type, suggesting a coherent biological
              mechanism worth pursuing in early discovery.
            </p>
          </section>

          <Separator />

          {/* Top Targets */}
          <section className="flex flex-col gap-4">
            <h3 className="text-foreground text-base font-semibold">
              Top Targets
            </h3>
            <div className="flex flex-col gap-4">
              {top.map((t, i) => (
                <div key={t.target_id} className="flex flex-col gap-1">
                  <div className="flex flex-wrap items-center gap-2">
                    <span className="text-foreground font-mono text-sm font-semibold">
                      {i + 1}. {t.symbol}
                    </span>
                    {t.evidence.map((e) => (
                      <Badge key={e.datatype} variant="outline">
                        {e.datatype.replace(/_/g, " ")}
                      </Badge>
                    ))}
                    <span className="text-muted-foreground ml-auto text-xs font-medium">
                      Score {t.score.toFixed(2)}
                    </span>
                  </div>
                  <p className="text-muted-foreground text-sm leading-relaxed">
                    <span className="text-foreground font-medium">
                      {t.name}
                    </span>{" "}
                    {t.explanation}
                  </p>
                </div>
              ))}
            </div>
          </section>

          <Separator />

          {/* Supporting Evidence */}
          <section className="flex flex-col gap-4">
            <h3 className="text-foreground text-base font-semibold">
              Supporting Evidence
            </h3>
            <p className="text-muted-foreground text-sm leading-relaxed">
              Key contributing factors behind the top-ranked targets:
            </p>
            <ul className="flex flex-col gap-3">
              {top.map((t) => (
                <li key={t.target_id} className="flex flex-col gap-1">
                  <p className="text-foreground text-sm leading-snug font-medium">
                    {t.symbol} &mdash; {t.name}
                  </p>
                  <p className="text-muted-foreground text-xs">
                    Top contributing features:{" "}
                    {t.top_contributions
                      .map(
                        (c) =>
                          `${c.feature.replace(/_/g, " ")} (${c.contribution.toFixed(2)})`,
                      )
                      .join(", ")}
                  </p>
                </li>
              ))}
            </ul>
          </section>
        </CardContent>
      </Card>

      <Card>
        <CardHeader>
          <CardTitle className="text-sm">Export</CardTitle>
        </CardHeader>
        <CardContent className="flex flex-col gap-3 sm:flex-row">
          <Button
            variant="outline"
            onClick={handleDownload}
            className="flex-1"
          >
            <Download data-icon="inline-start" />
            Download as PDF
          </Button>
          <Button
            onClick={saveCurrentReport}
            disabled={Boolean(activeReportId)}
            className="flex-1"
          >
            {activeReportId ? (
              <>
                <Check data-icon="inline-start" />
                Saved to Reports
              </>
            ) : (
              <>
                <Save data-icon="inline-start" />
                Save to Reports
              </>
            )}
          </Button>
        </CardContent>
      </Card>
    </div>
  )
}

function mostCommonEvidence(targets: ScoreResponse["targets"]): string {
  const counts = new Map<string, number>()
  for (const t of targets) {
    for (const e of t.evidence) {
      counts.set(e.datatype, (counts.get(e.datatype) ?? 0) + 1)
    }
  }
  let best = "genetic_association"
  let bestCount = 0
  for (const [item, count] of counts) {
    if (count > bestCount) {
      best = item
      bestCount = count
    }
  }
  return best.replace(/_/g, " ")
}
